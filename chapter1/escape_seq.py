# "\"  backslash is used for escape sequence 

print("Hello \"kamalakar\" Welcome")
print('Hello \'kamalakar\' Welcome')
print("Hello \"kamalakar\" Welcome \n")  #new line escape  sequence
print("Hello \\kamalakar\\ Welcome")   #new back slash  eacape sequence
print("Hello \tkamalakar\t Welcome")   #new tab  eacape sequence
print("Hello  \bkamalakar\b Welcome")   #backspace  eacape sequence

