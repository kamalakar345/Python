#printing a specfic index 

print(language[0]) #to print frist charcter in string
print(language[-1]) #to print last charcter in string
print(language[0:3]) # this will take n-1 charcterie (3 -1) =2, means 0,1,2 
print(language[-1:3])
print(language[3:])   #this will print  from  n charcterie 3,4,5  till last
print(language[0::2])  #by using extra in range we can print one char to char by 2 gap
